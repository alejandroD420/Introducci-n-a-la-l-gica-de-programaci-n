# Sacar el promedio de 3 notas diferentes

nota1 = float(input("Ingresar tu primera nota: "))
nota2 = float(input("Ingresar tu segunda nota: "))
nota3 = float(input("Ingresar tu tercera nota: "))

promedio = ((nota1 + nota2 + nota3)/3)

print(f"La nota 1 = {nota1} nota 2 = {nota2} nota 3 = {nota3}")

print(f"De modo que el promedio es {promedio}")
