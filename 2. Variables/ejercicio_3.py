#  Calcular el area de un rectangulo

base = float(input("Ingresar la longitud de la base del rectangulo: "))
altura = float(input("Ingresar la longitud de la altura del rectangulo: "))

area = base * altura

print(f"La base del rectangulos es: {base}")
print(f"La altura del rectangulos es: {altura}")
print(f"El area del rectangulos es: {area}")

